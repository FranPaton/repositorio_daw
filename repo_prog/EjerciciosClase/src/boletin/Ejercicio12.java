package boletin;

public class Ejercicio12 {

	public static void main(String[] args) {
		
		char a = 'a';
		char b = 'b';
		char c = 'c';
		char d = 'd';
		char e = 'e';
		
		System.out.println(a + b + c + d + e);
		
		System.out.println(a + "" + b + "" + c + "" + d +  "" + e);
		
		System.out.println(Character.toString(a) + Character.toString(b) +  Character.toString(c) +  Character.toString(d) +  Character.toString(e) );
		
		System.out.println(String.valueOf(a) + String.valueOf(b) + String.valueOf(c) + String.valueOf(d) + String.valueOf(e));
		

	}

}
